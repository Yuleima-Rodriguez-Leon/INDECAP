<h3>PREPARACION DE LA BASE DEL FLAN DE LECHE CONDENSADA</h3>
<ul>
<li>En un cuenco batimos los huevos con la leche entera y la leche condensada sin hacer demaciada espuma. Lojusto para que mezclen y quede una masa cremosa</li>
<li>Rellenamos la bandeja central del hornocon agua caliente hasta que cubra un tercio de la altura del molde</li>
<li>subimos la temperatura del horno a 200° C hasta que empiece a hervir el agua</li>
<li>ponemos la la crema de flan de leche condensada en pequeños moldes de flan o en una flanera individual</li>
<li>Lo cubrimos con papel aluminio y los metemos al horno en la bandeja con el agua durante 50-60 minutos aproximadamente</li>
<li>El tiempo depende un poco del tamaño del molde o moldes y del horno, a partir de los 40 minutos, comprobar la coccion</li>
</ul>
<section>
<h3>HORNEADO Y PRESENTACION FINAL DEL FLAN</h3>
<ul>
<li>Horneamos a unos a unos 200°C hasta que el flan o los flanes individuales esten cuajados y dorados</li>
<li>Al cabo de los 50 minutos se puede probar con un cuchillo para ver si estan totalmente cuajados, si salen limpios ya estan listos, si no dejar un poquito mas</li>
<li>Dejamos enfriar a temperatura ambiente y despues la metemos a la nevera</li>
<li>Desmoldamos con cuidado en un plato para que se bañe todo el flan con el caramelo</li>
